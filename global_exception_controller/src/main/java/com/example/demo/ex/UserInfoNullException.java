public class UserInfoNullException extends GlobalException{
    public UserInfoNullException() {
    }

    public UserInfoNullException(String message) {
        super(message);
    }

    public UserInfoNullException(String message, Throwable cause) {
        super(message, cause);
    }

    public UserInfoNullException(Throwable cause) {
        super(cause);
    }

    public UserInfoNullException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}

